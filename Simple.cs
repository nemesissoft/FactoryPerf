    sealed class SimpleFactory
    {
        private readonly Dictionary<string, Type> _types;

        private SimpleFactory(Dictionary<string, Type> types = null) => _types = types ?? new(StringComparer.OrdinalIgnoreCase);

        //public void Register(string name, Type t) => _types[name] = t;

        public static SimpleFactory GetDefault() =>
            new(
                Assembly.GetExecutingAssembly().GetTypes().Where(t => t.IsClass && t.IsPublic && !t.IsAbstract)
                    .ToDictionary(t => t.Name)
            );

        public object Create(string name)
        {
            if (_types.TryGetValue(name, out var type) && type != null && type.GetConstructor(Type.EmptyTypes) is { } ctor)
                return ctor.Invoke(null);
            else return null;
        }
    }

